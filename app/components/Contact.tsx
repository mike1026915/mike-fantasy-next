interface IconType {
  viewBox: string;
  path: string;
}

interface Props {
  name: string;
  icon: IconType|undefined;
}

const Icon = (props: Props) => {
  const {
      name,
      icon,
  } = props;

  return (
    <svg
      viewBox={icon?.viewBox}
      style={(name === 'linkedin') ? { marginBottom: '1px', } : {}}
      className="h-5 w-5 fill-gray-100 mx-1"
    >
      <title>{name}</title>
      <path d={icon?.path} />
    </svg>
  );
}

const getIcon = (name: string): IconType|undefined => {
  let icon;

  switch (name) {
    case 'github':
      icon = ICONS.GITHUB as IconType;
      break;
    case 'email':
      icon = ICONS.EMAIL as IconType;
      break;
    case 'linkedin':
      icon = ICONS.LINKEDIN as IconType;
      break;
    case 'instagram':
      icon = ICONS.INSTAGRAM ;
      break;
  }

  return icon;
};

